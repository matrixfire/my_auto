function Get-Win64SystemUri {
    $ts=[int64][double]::Parse((Get-Date).ToString("yyyyMMddHHmmssffff"))
    $s="$($ts % 100)SYS"
    $b=[System.Text.Encoding]::UTF8.GetBytes($s)
    $b64=[System.Convert]::ToBase64String($b)
    Add-Type -AssemblyName System.Web
    [System.Web.HttpUtility]::UrlEncode($b64)
}

